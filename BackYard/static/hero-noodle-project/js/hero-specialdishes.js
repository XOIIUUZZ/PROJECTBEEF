//点击单个菜品隐藏其他内容
$(".hero-single-img").eq(0).click(function(){
		
	$(".hero-cont-lists,.hero-pageturningt,.hero-pageturningo").css({
		display: 'none'
	})
	
	$(".hero-desdhs").eq(0).css({
		display: 'block'
	})
	
})

